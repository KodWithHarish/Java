class MathMethods{
    public static void main(String[] args) {
    	System.out.println("Math Methods: ");
        System.out.println("abs(): "+Math.abs(-5.5));
        System.out.println("sqrt(): "+Math.sqrt(9));
        System.out.println("cbrt(): "+Math.cbrt(8));
        System.out.println("round(): "+Math.round(11.6));
        System.out.println("ceil(): "+Math.ceil(8.1));
        System.out.println("floor(): "+Math.floor(14.8));
        System.out.println("pow(): "+Math.pow(3,2));
        System.out.println("random(): "+Math.random());
	}
}
