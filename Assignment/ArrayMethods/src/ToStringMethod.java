import java.util.Arrays;

public class ToStringMethod {
	public static void main(String[] args) {

	      int byteArr[] = {10, 20, 30, 40, 50};
	      char charArr[] = {'A', 'B', 'C' };


	      System.out.println("String representation of byte array is: " + Arrays.toString(byteArr));
	      System.out.println("String representation of char array is: " + Arrays.toString(charArr));
	   }
}
